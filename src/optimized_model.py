    print("\nTop 3 models by Accuracy:")
    print(results_df.sort_values('Accuracy', ascending=False).head(3).to_string(index=False))

    candidates = list(top3_models)
    if 'Ensemble (Voting)' in name_to_pred:
        candidates.append('Ensemble (Voting)')

    # 1) Print full metrics for each
    print("\nDetailed metrics (Accuracy, Precision, Recall, F1, AUC):")
