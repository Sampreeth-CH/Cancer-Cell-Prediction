# =========================
# Expanded Feature Engineering Workflow
# =========================
# 1) Base features and target
X_base = df.drop(columns=['Patient_ID', 'Cancer_Diagnosis'])
y = df['Cancer_Diagnosis']

# 2) Identify remaining categoricals and apply OneHotEncoder
categorical_cols_dynamic = X_base.select_dtypes(include=['object', 'category']).columns.tolist()
numeric_cols_dynamic = X_base.columns.difference(categorical_cols_dynamic).tolist()

if len(categorical_cols_dynamic) > 0:
    ohe = OneHotEncoder(handle_unknown='ignore', sparse=False)
    X_ohe = ohe.fit_transform(X_base[categorical_cols_dynamic].astype(str))
    ohe_feature_names = ohe.get_feature_names_out(categorical_cols_dynamic)
    X_ohe_df = pd.DataFrame(X_ohe, columns=ohe_feature_names, index=X_base.index)
else:
    X_ohe_df = pd.DataFrame(index=X_base.index)

X_numeric_df = X_base[numeric_cols_dynamic].copy()

# 3) Simple encoded matrix and fill missing
X_encoded = pd.concat([X_numeric_df, X_ohe_df], axis=1).fillna(0)

# 4) Ensure categorical encodings exist
for col in categorical_cols_dynamic:
    if not any([c.startswith(col + '_') for c in X_encoded.columns]):
        X_encoded[col + '_missing'] = 0

# 5) Polynomial features (degree=2) on numeric columns only
if len(X_numeric_df.columns) > 0:
    poly = PolynomialFeatures(degree=2, include_bias=False)
    X_poly = poly.fit_transform(X_numeric_df)
    poly_feature_names = poly.get_feature_names_out(X_numeric_df.columns)
    X_poly_df = pd.DataFrame(X_poly, columns=poly_feature_names, index=X_numeric_df.index)
else:
    X_poly_df = pd.DataFrame(index=X_numeric_df.index)

# 6) Interaction features among first few numeric columns
interaction_cols = numeric_cols_dynamic[:min(5, len(numeric_cols_dynamic))]
X_interact_df = pd.DataFrame(index=X_base.index)
for i in range(len(interaction_cols)):
    for j in range(i + 1, len(interaction_cols)):
        c_i, c_j = interaction_cols[i], interaction_cols[j]
        X_interact_df[f"{c_i}__x__{c_j}"] = X_base[c_i] * X_base[c_j]

# 7) Candidate feature sets
feature_sets = {
    'encoded_only': X_encoded,
    'encoded_plus_poly': pd.concat([X_encoded, X_poly_df], axis=1),
    'encoded_plus_interact': pd.concat([X_encoded, X_interact_df], axis=1)
}

# 8) SelectKBest on encoded features
X_for_selection = feature_sets['encoded_only']
if X_for_selection.shape[1] > 0:
    k_select_fs = max(10, int(0.5 * X_for_selection.shape[1]))
    selector_fe = SelectKBest(score_func=f_classif, k=min(k_select_fs, X_for_selection.shape[1]))
    X_selected = selector_fe.fit_transform(X_for_selection, y)
    selected_feature_names = X_for_selection.columns[selector_fe.get_support(indices=True)]
    X_selected_df = pd.DataFrame(X_selected, columns=selected_feature_names, index=X_for_selection.index)
    feature_sets['encoded_selectKBest'] = X_selected_df

# 9) Evaluate feature sets with quick 3-fold CV (Logistic Regression) to pick best
cv = StratifiedKFold(n_splits=3, shuffle=True, random_state=42)
set_scores = {}
for set_name, X_fs in feature_sets.items():
    if X_fs.shape[1] == 0:
        continue
    scaler_tmp = StandardScaler()
    X_fs_scaled = scaler_tmp.fit_transform(X_fs)
    lr_tmp = LogisticRegression(max_iter=500, random_state=42)
    f1_scores_tmp = []
    for tr_idx, te_idx in cv.split(X_fs_scaled, y):
        X_tr_tmp, X_te_tmp = X_fs_scaled[tr_idx], X_fs_scaled[te_idx]
        y_tr_tmp, y_te_tmp = y.iloc[tr_idx], y.iloc[te_idx]
        lr_tmp.fit(X_tr_tmp, y_tr_tmp)
        y_pred_tmp = lr_tmp.predict(X_te_tmp)
        f1_scores_tmp.append(f1_score(y_te_tmp, y_pred_tmp, zero_division=0))
    set_scores[set_name] = float(np.mean(f1_scores_tmp))

if len(set_scores) > 0:
    best_set_name = max(set_scores, key=set_scores.get)
