label_encoders = {}
for col in ['Gender', 'Smoking_Status', 'Alcohol_Use', 'Family_History']:
    if col in df.columns and df[col].dtype == object:
        le = LabelEncoder()
        df[col] = le.fit_transform(df[col].astype(str))
