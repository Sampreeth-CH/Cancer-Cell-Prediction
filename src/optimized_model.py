"""
Cancer Prediction Pipeline (Simplified, Separate File)

Steps:
1) Categorical handling: detect, OneHotEncode, fill missing
2) Advanced features: Polynomial (degree=2), domain interactions, SelectKBest, CV to choose set
3) Train-test split + SMOTE on train only
4) Load best hyperparameters (if available)
5) Define and train individual models (LR, RF, GB)
6) Select top-3 by accuracy → VotingClassifier (soft)
7) Evaluate, visualize (CM, ROC), and save models/artifacts
"""

