    final_feature_names = X_all.columns.tolist()

    # Prepare final set (the chosen best set)
    X_final = X_all.values
    X_train, X_test, y_train, y_test = train_split_smote(X_final, y_all)

    # Define base models
    models = {
        'Logistic Regression': LogisticRegression(max_iter=1000, random_state=42),
        'Random Forest': RandomForestClassifier(n_estimators=300, max_depth=20, max_features='sqrt', random_state=42, n_jobs=-1),
        'Gradient Boosting': GradientBoostingClassifier(n_estimators=300, learning_rate=0.05, max_depth=5, random_state=42)
    }

    # Load best params if available
    models = load_best_params(models)

    # Train/evaluate individual models
    results = []
    name_to_pred = {}
    name_to_proba = {}
    name_to_ytrue = {}
    for name, mdl in models.items():
        mdl.fit(X_train, y_train)
        m, y_pred, y_proba = eval_model(name, mdl, X_test, y_test)
        results.append(m)
        name_to_pred[name] = y_pred
        name_to_proba[name] = y_proba
        name_to_ytrue[name] = y_test
        plot_and_save_confusion_matrix(name, y_test, y_pred)

    results_df = pd.DataFrame(results)
    results_df.to_excel('Simplified_Model_Metrics.xlsx', index=False)

    # Select top-3 by Accuracy for VotingClassifier
    top3 = results_df.sort_values('Accuracy', ascending=False).head(3)['Model'].tolist()
    voting_estimators = [(k.lower().split()[0], models[k]) for k in top3]
    voting = VotingClassifier(estimators=voting_estimators, voting='soft')
    voting.fit(X_train, y_train)
    mv_metrics, mv_pred, mv_proba = eval_model('Ensemble (Voting)', voting, X_test, y_test)
    results_df = pd.concat([results_df, pd.DataFrame([mv_metrics])], ignore_index=True)
